def get_related(art_num, cat_key):
    cats = CATEGORIES[cat_key]
    all_nums = cats[2]
    others = [n for n in all_nums if n != art_num][:4]
    return others


def make_content(art_num, art_data):
    title, cat_name, cat_key, desc, paras = art_data
    cat_link = CATEGORIES[cat_key][1]
    cats = CATEGORIES[cat_key]
    related = get_related(art_num, cat_key)

    body_paras = "".join(f"<p>{p}</p>" for p in paras)

    related_html = ""
    if related:
        related_html = f"""<div class="related-articles">
<h3>📌 相关推荐</h3>
<ul>"""
        for r in related:
            rtitle = ARTICLES[r][0]
            related_html += f'<li><a href="article{r}.html">{rtitle}</a></li>'
        related_html += "</ul></div>"

    pub_date = f"2026-{3+(art_num//20):02d}-{(art_num%28)+1:02d}"

    html = f'''<!DOCTYPE html>
<html lang="zh-CN">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>{title} - 办公小课</title>
  <meta name="description" content="办公小课{cat_name}：{desc}">
  <link rel="stylesheet" href="css/style.css">
</head>
<body>
{NAV}
<div class="container">
  <div class="breadcrumb">
    <a href="index.html">首页</a> &gt; <a href="{cat_link}">{cat_name}</a> &gt; {title}
  </div>
  <div class="ad-banner">【百度联盟广告位5】</div>
  <div class="article-detail">
    <h1 class="article-title">{title}</h1>
    <div class="article-info">{pub_date} &nbsp;|&nbsp; {cat_name}</div>
    <div class="article-content">
      {body_paras}
    </div>
  </div>
  <div class="ad-banner">【百度联盟广告位6】</div>
  {related_html}
</div>
{FOOTER}
<script src="js/main.js"></script>
</body>
</html>'''
    return html


def main():
    articles_dir = os.path.join(BASE, "articles")
    os.makedirs(articles_dir, exist_ok=True)
    count = 0
    for art_num, art_data in ARTICLES.items():
        html = make_content(art_num, art_data)
        path = os.path.join(articles_dir, f"article{art_num}.html")
        with open(path, "w", encoding="utf-8") as f:
            f.write(html)
        count += 1
    print(f"✅ 成功生成 {count} 篇详情页！")


if __name__ == "__main__":
    main()
